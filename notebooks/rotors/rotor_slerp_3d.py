@app.cell(hide_code=True)
def _(mo):
    mo.md(r"""
    ## Slerp Between Different Rotation Planes

    Given two rotors $R_0$ and $R_1$, we form the relative rotor

    $$
    \Delta = \widetilde{R_0} R_1
    $$

    and interpolate by

    $$
    R(t) = R_0 \exp\!\bigl(t \log(\Delta)\bigr).
    $$

    The same formula works even when $R_0$ and $R_1$ come from different bivector planes. That is where the geometric algebra viewpoint starts to earn its keep.
    """)
    return


