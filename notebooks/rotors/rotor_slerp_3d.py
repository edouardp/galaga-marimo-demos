@app.cell
def _(np, plt):
    def draw_slerp_3d(v, v0, v1, vt):
        fig = plt.figure(figsize=(7, 6))
        ax = fig.add_subplot(111, projection="3d")

        u = np.linspace(0, 2 * np.pi, 32)
        w = np.linspace(0, np.pi, 18)
        x = np.outer(np.cos(u), np.sin(w))
        y = np.outer(np.sin(u), np.sin(w))
        z = np.outer(np.ones_like(u), np.cos(w))
        ax.plot_wireframe(x, y, z, color="gray", alpha=0.08)

        def arrow(vec, color, label):
            vx, vy, vz = vec.vector_part[:3]
            ax.quiver(0, 0, 0, vx, vy, vz, color=color, linewidth=2.5, arrow_length_ratio=0.08, label=label)

        arrow(v, "black", "input vector")
        arrow(v0, "steelblue", "R0 v R̃0")
        arrow(v1, "darkorange", "R1 v R̃1")
        arrow(vt, "crimson", "R(t) v R̃(t)")

        ax.set_xlim(-1.1, 1.1)
        ax.set_ylim(-1.1, 1.1)
        ax.set_zlim(-1.1, 1.1)
        ax.set_box_aspect((1, 1, 1))
        ax.set_xlabel("e1")
        ax.set_ylabel("e2")
        ax.set_zlabel("e3")
        ax.set_title("3D action of slerped rotors")
        ax.legend(loc="upper left")
        plt.close(fig)
        return fig

    return (draw_slerp_3d,)


